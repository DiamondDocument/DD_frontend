<template>
  <div>
    <div v-if="isOwner">
      <el-tabs v-model="cardSite" type="border-card" @tab-click="clean">
        <el-tab-pane label="个人信息" name="1">

          <input type="file"
                 ref="clearFile"
                 style="display:none"
                 @change="upload($event)"/>

          <el-avatar :size="200" :src="circleUrl" style="float: left; " @click="changeImg"/>
          <el-form
              label-position="Right"
              label-width="100px"
              style="
              max-width: 300px;
              margin: 20px;
        ">
            <el-form-item label="用户名：">
              {{ userId }}
            </el-form-item>

            <el-form-item label="用户昵称：">
              {{ nickName }}
            </el-form-item>

            <el-form-item label="邮箱：">
              {{ email }}
            </el-form-item>

            <el-form-item label="用户简介：">
              {{ introduction }}
            </el-form-item>
          </el-form>

        </el-tab-pane>

        <!--      切换清空 邮箱 密码 时无法消除错误提示    -->


        <el-tab-pane label="修改信息" name="2">
          <el-form
              label-position="Right"
              label-width="100px"
              style="max-width: 460px"
          >
            <el-form-item label="用户昵称：">
              <el-input type="text"
                        style="margin-bottom: 10px"
                        v-model="c_nickName"
                        />
            </el-form-item>

            <el-form-item label="用户简介：">
              <el-input type="textarea" :autosize="{ minRows: 2, maxRows: 4 }"
                        style="margin-bottom: 10px"
                        v-model="c_introduction"
              />
            </el-form-item>

            <el-form-item label="邮箱：">
              <el-input type="text"
                        style="margin-bottom: 10px"
                        v-model="c_email"
                        @blur="checkEmail">
                <template #append>
                  <el-button  @click="sendCode" :disabled="!(emailCheckRes === 0)">发送验证码</el-button>
                </template>
              </el-input>
              <p v-if="emailCheckRes === 1" style="
               font-size: 12px;
               color: red;
          ">
                邮箱格式错误
              </p>
            </el-form-item>

            <el-form-item label="验证码">
              <el-input type="text"
                        v-model="c_code"
                        style="
                      width: 200px;
                      float: left ;"/>
            </el-form-item>


          </el-form>
          <el-button type="success"
                     @click="commit"
                     style="margin-left: 100px">
            提交修改
          </el-button>
        </el-tab-pane>


        <el-tab-pane label="修改密码" name="3">
          <el-form
              label-position="Right"
              label-width="100px"
              style="max-width: 460px"
          >
            <el-form-item label="原密码：">
              <el-input v-model="oldPwd" type="password"/>
            </el-form-item>

            <el-form-item label="新密码：">
              <el-input v-model="newPwd" type="password" @blur="checkPwd"/>
              <p v-if="(pwdCheckRes === 1 || pwdCheckRes === 2)" style="
               font-size: 12px;
               color: red;
            ">
                密码只能由6-20个数字，英文字母或下划线组成
              </p>
            </el-form-item>



            <el-form-item label="确认密码：">
              <el-input v-model="confirm" type="password" />
            </el-form-item>

            <div style="margin-left: 200px;">
              <el-button type="success" @click="changePwd">
                提交修改
              </el-button>
            </div>

          </el-form>
        </el-tab-pane>

      </el-tabs>
    </div>

    <div v-if="!isOwner">
      <div style="float: left; margin-left: 20px;">
        <el-avatar :size="200" :src="url" />
      </div>

      <div >
        <el-form
            label-position="Right"
            label-width="100px"
            style="
              max-width: 300px;
              margin: 20px;
        ">
          <el-form-item label="用户名：">
            {{ userId }}
          </el-form-item>

          <el-form-item label="用户昵称：">
            {{ nickName }}
          </el-form-item>

          <el-form-item label="邮箱：">
            {{ email }}
          </el-form-item>

          <el-form-item label="用户简介：">
            {{ introduction }}
          </el-form-item>
        </el-form>
      </div>

    </div>

  </div>
</template>

<script>
import qs from "qs";
import {ElMessage} from "element-plus";

export default {

  name: "Information",
  data(){
    return {
      isOwner: 1,
      userId: '',
      url: '',
      cardSite: '1',
      nickName: '',
      email: '',
      introduction: '',
      c_nickName: '',
      c_email: '',
      c_introduction: '',
      c_code: '',
      identifyingCode: '',
      oldPwd: '',
      newPwd: '',
      confirm: '',
      emailCheckRes: -1,
      pwdCheckRes: -1,
      pwdJudge: /^\w+$/,
      emailJudge: /^\w+([-+.]\w+)*@\w+([-.]\w+)*\.\w+([-.]\w+)*$/,
    }
  },
  methods: {
    clean: function (){
      this.c_nickName = '';
      this.c_introduction = '';
      this.c_email = '';
      this.c_code = '';
      this.newPwd = '';
      this.oldPwd = '';
    },

    checkEmail: function (){    //检查邮箱格式
      if (this.c_email === ''){
        this.emailCheckRes = -1;
        return;
      }
      if (this.emailJudge.test(this.c_email)) this.emailCheckRes = 0;
      else this.emailCheckRes = 1;
    },

    checkPwd: function (){
      if (this.newPwd === ''){
        this.pwdCheckRes = -1;
        return;
      }
      if (this.newPwd.length < 6 || this.newPwd.length > 20){
        this.pwdCheckRes = 2;
        return;
      }
      if (this.pwdJudge.test(this.newPwd)) this.pwdCheckRes = 0;
      else  this.pwdCheckRes = 1;
    },

    changeImg: function (){
      console.log("changeImg is called!");
      this.$refs.clearFile.click();
    },

    upload: function(e){
      console.log("upload is called!")
      let getFile =document.getElementById("files");
      // getFile.onchange=function(e){
      //获取到文件以后就会返回一个对象，通过这个对象即可获取文件
      console.log(e.currentTarget.files);//所有文件，返回的是一个数组
      console.log(e.currentTarget.files[0].name)//文件名
      let form = new FormData();
      form.append("file",e.currentTarget.files[0]);
      form.append("userId", this.$store.state.loginUser.userId)
      this.axios.post("/api/user/modify/avatar",form).then((response)=>{
        if(response.status === 200){
          if (response.data.code === 0){
            ElMessage("上传成功！");
            console.log(response.data);
          }else if (response.data.code === 1) ElMessage("上传失败")
        }else console.log("status is not 200!");
      }).catch((err)=>{
        console.log(err);
      });
      // }
    },

    changeNickname: function (){
      console.log('changeNickname is called!')
      this.$axios.post("/api/user/modify/nickname",
          {
            "userId" : this.userId,
            "newNick" : this.c_nickName,
          }).then((res)=>{
        if (res.status === 200){
          if (res.data.code === 0) ElMessage("修改成功！");
          else ElMessage("系统错误！！");
        }else console.log("return status != 200!!");
      }).catch((err)=>{
        console.log(err);
      })
    },

    changeIntroduction: function (){
      console.log('changeIntroduction is called!');
      this.$axios.post("/api/user/modify/introduction",
          {
            "userId" : this.userId,
            "newIntro" : this.c_introduction,
          }).then((res)=>{
        if (res.status === 200){
          if (res.data.code === 0) ElMessage("修改成功！");
          else if (res.data.code === 1) ElMessage("用户不存在");
          else ElMessage("系统错误！！");
        }else console.log("return status != 200!!");
      }).catch((err)=>{
        console.log(err);
      })
    },

    changeEmail: function (){
      console.log('changeEmail is called !');
      this.$axios.post("/api/user/modify/email",
          {
            "userId" : this.userId,
            "newEmail" : this.c_email,
            "verificationCode": this.c_code,
          }).then((res)=>{
        if (res.status === 200){
          if (res.data.code === 0) ElMessage("修改成功！");
          else if (res.data.code === 1) ElMessage("验证码错误！");
          else ElMessage("系统错误！！");
        }else console.log("return status != 200!!");
      }).catch((err)=>{
        console.log(err);
      })
    },

    commit: function (){
      this.changeNickname();
      this.changeIntroduction();
      this.changeEmail();

      this.getInformation();
      this.cardSite = '1';
      this.clean();
    },

    sendCode: function (){
      this.$axios.get("/api/user/send-identifying", {
        params:{
          email: this.c_email,
        }
      }).then((response)=>{
        if (response.status === 200){
          if (response.data.code === 0){
            ElMessage("发送成功");
          }else ElMessage("发送失败");
        }else console.log("请求返回status不为200")
      }).catch((err)=>{
        console.log(err);
      });
    },

    changePwd :function (){
      if (!(this.newPwd === this.confirm)){
        ElMessage('两次输入密码不同');
        return;
      }
      this.$axios.post("/api/user/modify/password",
          {
            "userId" : this.userId,
            "newPwd" : this.newPwd,
            "oldPwd" : this.oldPwd,
          }).then((res)=>{
        if (res.status === 200){
          if (res.data.code === 0) ElMessage("修改成功！");
          else if (res.data.code === 1) ElMessage("原密码错误");
          else ElMessage("系统错误！！");
        }else console.log("return status != 200!!");
      }).catch((err)=>{
        console.log(err);
      })

      location.reload();
    },

    getInformation: function (){
      this.$axios.get("/api/user/information", {
        params:{
          userId: this.userId,
        }
      }).then((response)=>{
        if (response.status === 200){
          if (response.data.code === 0){
            this.nickName = response.data.nickName;
            this.email = response.data.email;
            this.introduction = response.data.introduction;
          }else if(response.data.code === 1) console.log('用户不存在')
          else console.log("用户信息获取错误");
        }else console.log("请求返回status不为200")
      }).catch((err)=>{
        console.log(err);
      });
    },

    getAvatar: function (){
      this.$axios.get("/api/user/get-avatar", {
        params:{
          userId: this.userId,
        }
      }).then((response)=>{
        if (response.status === 200){
          if (response.data.code === 0){
            this.url = response.data.url;
          }else console.log("用户头像获取错误");
        }else console.log("请求返回status不为200")
      }).catch((err)=>{
        console.log(err);
      });
    },
  },

  created() {
    this.isOwner = (this.$route.params.userId === this.$store.state.loginUser.userId);

    console.log('' + this.$route.params.userId + ' - ' + this.$store.state.loginUser.userId);
    this.userId = this.$route.params.userId;

    this.getInformation();
    this.getAvatar();
  }
}
</script>

