  methods: {
    isBelong() {
      this.$axios.get("/team/team-status", {
        params:{
          teamId: this.TeamId,
          userId: this.userId
        }
      }).then((response)=>{
        if (response.status === 200){
          if (response.data.code === 0){
            this.belong = (response.data.status === 0 || response.data.status === 1);
          }else console.log("团队信息获取错误");
        }else console.log("请求返回status不为200")
      }).catch((err)=>{
        console.log(err);
      });

      console.log("belong " + this.belong)
    },
    toMessage() {
      this.type = 'message';
    },
    goInformation: function (){
      this.$router.push({name: 'userInformation', params: {userId: this.$store.state.loginUser.userId}});
    },
    toMyCollection() {
      this.type='collection'
    },
    toBin() {
