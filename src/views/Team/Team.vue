<template>
  <div>

    <div class="information"
         style="height:180px;
                width: 100%;
                background: #ebebeb;
                position: absolute"
    >

      <el-image v-if="userType === 0" @click="changeImg" class="teamImg" :src="team_img" fit="fill" >
      </el-image>
      <el-image v-else class="teamImg" :src="team_img" fit="fill" >
      </el-image>
      <div style="float: left; padding: 10px" v-if="changing === 0">
        <h2 class="teamName">
          {{ team_name }}
        </h2>
        <p class="mainText">
          {{ team_introduction }}
        </p>
      </div>

      <div style="float: left; padding: 10px" v-if="changing === 1">
        <el-input type="text"
                  v-model="c_teamName"
                  placeholder="请输入团队名称"/>
        <el-input type="textarea"
                  v-model="c_teamIntroduction"
                  placeholder="请输入团队简介"
                  style="margin-top: 10px"/>
      </div>



      <div style="
      height: 180px;
      float: right;
      margin: auto 20px;">

        <div style="margin-top: 20px">
          <el-button v-if="userType === 0" type="danger" @click="dropTeam">
            解散团队
          </el-button>

          <el-button v-else-if="userType === 1" type="danger" @click="leaveTeam">
            离开团队
          </el-button>

          <el-button v-else-if="userType === 2" type="info" disabled="true">
            待审核
          </el-button>

          <el-button v-else-if="userType === 3" type="success" @click="accept">
            同意邀请
          </el-button>

          <el-button v-else-if="userType === 4" type="success" @click="apply">
            申请加入
          </el-button>

          <el-button v-else type="info" disabled="true">
            你究竟是谁？？
          </el-button>
        </div>

        <el-button v-if="userType === 0"
                   @click="invite"
                   type="primary"
                   style="margin-top: 10px">
          邀请新成员
        </el-button>
        <br>

        <el-button @click="enterTable"
                   type="success"
                   style="margin-top: 10px">
          进入团队工作台
        </el-button>



      </div>

      <div style="
           margin-top: 120px;
      ">
        <el-button v-if="userType === 0 && changing === 0"
                   @click="change">
          修改
        </el-button>
        <input type="file"
                  ref="clearFile"
                  style="display:none"
                  @change="upload($event)"/>
        <el-button v-if="userType === 0 && changing === 1"
                   @click="changeImg">
        修改头像
        </el-button>
        <el-button v-if="userType === 0 && changing === 1"
                   @click="endChange">
          完成
        </el-button>
      </div>


    </div>

    <div style="margin-top: 10px;
                position: relative;
                top:180px;
                padding: 20px ">
      <el-row v-for="mem in memList" :key="mem.id" class="block">
        <div @click="goUser(mem.id)"
             style="
             float: right;
             width: 80%">
          <el-avatar src="circleUrl" style="float: left; margin-top: 20px"/>
          <div style="float: left; margin-left: 25px; margin-top: 25px">
            {{mem.nickName}}
          </div>
        </div>
        <div v-if="userType === 0"
             style="
             height: 80px;
              position: absolute;
              right: 15px;
              float: right;">
          <el-button  type="danger" style="margin: 5px" @click="removeMem">
            移除成员
          </el-button>
          <br>
          <el-button style="margin: 5px" @click="transPri">
            转让权限
          </el-button>
        </div>

      </el-row>
    </div>

  </div>
</template>

<script>
import {ElMessage} from "element-plus";

export default {
  data(){
    return {
      changing : 0,
      userType: 0,
      teamId: '',
      team_img:"../../assets/logo.png",
      team_name: "软工",
      team_introduction: "for test",
      c_teamName: '',
      c_teamIntroduction: '',
      memList: [
        {
          id: 1,
          nickName: "小王"
        },
        {
          id: 2,
          nickName: "小李"
        }
      ]
    }
  },

  methods: {
    change: function (){
      this.changing = 1;
    },

    enterTable: function (){
      this.$router.push({name: 'table', params: {info: 'team-' + this.teamId}});
    },

    endChange: function (){
      this.changing = 0;
      this.$axios.post("/api/team/modify/name",
          {
            "teamId" : this.teamId,
            "newName": this.c_teamName
          }).then((res)=>{
        if (res.status === 200){
          if (res.data.code === 0) ElMessage("修改团队名称成功！");
          else if (res.data.code === 1) ElMessage("团队不存在");
          else ElMessage("系统错误！！");
        }else console.log("return status != 200!!");
      }).catch((err)=>{
        console.log(err);
      })

      this.$axios.post("/api/team/modify/name",
          {
            "teamId" : this.teamId,
            "newIntro": this.c_teamIntroduction
          }).then((res)=>{
        if (res.status === 200){
          if (res.data.code === 0) ElMessage("修改团队简介成功！");
          else if (res.data.code === 1) ElMessage("团队不存在");
          else ElMessage("系统错误！！");
        }else console.log("return status != 200!!");
      }).catch((err)=>{
        console.log(err);
      })
    },

    changeImg: function (){
      console.log("changeImg is called!");
      this.$refs.clearFile.click();
    },

    upload: function(e){
      console.log("upload is called!")
      let getFile =document.getElementById("files");
      // getFile.onchange=function(e){
      //获取到文件以后就会返回一个对象，通过这个对象即可获取文件
      console.log(e.currentTarget.files);//所有文件，返回的是一个数组
      console.log(e.currentTarget.files[0].name)//文件名
      let form = new FormData();
      form.append("file",e.currentTarget.files[0]);
      form.append("userId", this.$store.state.loginUser.userId)
      this.axios.post("/api/team/modify/avatar",form).then((response)=>{
        if(response.status === 200){
          ElMessage("上传成功！");
          console.log(response.data);
        }else{
          ElMessage("上传失败!");
          console.log("status is not 200!");
        }
      }).catch((err)=>{
        console.log(err);
      });
      // }
    },

    invite: function (){
      this.$router.push({name: 'teamInvite', params: {key: ''}});
    },

    dropTeam: function (){
      console.log("dropTeam is called!")
    },

    leaveTeam: function (){
      console.log("leaveTeam is called!")
    },

    accept: function (){
      console.log("accept is called!")
    },

    apply: function (){
      console.log("apply is called!")
    },

    goUser: function (userId){
      console.log("goUser is called");
      this.$router.push({name: 'userInformation', params: {userId: userId}});
    },

    removeMem: function (){
      console.log("removeMem is called!");
    },

    transPri: function (){
      console.log("transPri is called!");
    },

    checkUserType: function (){
      console.log("checkUserType is called!");
    }
  },
  created() {
    this.teamId = this.$route.params.teamId;
    this.checkUserType();

  }
}

</script>

